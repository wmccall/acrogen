function words(options) {
  function word(wordList) {
    if (options && options.maxLength > 1) {
      return generateWordWithMaxLength(wordList);
    } else {
      return generateRandomWord(wordList);
    }
  }

  function generateWordWithMaxLength(wordList) {
    var rightSize = false;
    var wordUsed;
    while (!rightSize) {
      wordUsed = generateRandomWord(wordList);
      if (wordUsed.length <= options.maxLength) {
        rightSize = true;
      }
    }
    return wordUsed;
  }

  function generateRandomWord(wordList) {
    let workKeys = Object.keys(wordList);
    let key = workKeys[randInt(workKeys.length)];
    return [key, wordList[key]];
  }

  function randInt(lessThan) {
    return Math.floor(Math.random() * lessThan);
  }

  // No arguments = generate one word
  if (typeof options === 'undefined') {
    return word(allWords);
  }

  // Just a number = return that many words
  if (typeof options === 'number') {
    options = { exactly: options };
  }

  if (options.ids) {
    let words = options.ids.map(id => {
      console.log(id);
      let word = allWords[id];
      return [id, word];
    });
    return words.filter(function(el) {
      console.log(el);
      return el[1] != null;
    });
  }

  // options supported: exactly, min, max, join
  if (options.exactly) {
    options.min = options.exactly;
    options.max = options.exactly;
  }

  let useWords = allWords;
  if (typeof options.letter === 'string') {
    if (letters.indexOf(options.letter) > -1) {
      useWords = letterWords[options.letter];
    }
  }

  // not a number = one word par string
  if (typeof options.wordsPerString !== 'number') {
    options.wordsPerString = 1;
  }

  //not a function = returns the raw word
  if (typeof options.formatter !== 'function') {
    options.formatter = word => word;
  }

  //not a string = separator is a space
  if (typeof options.separator !== 'string') {
    options.separator = ' ';
  }

  var total = options.min + randInt(options.max + 1 - options.min);
  var results = [];
  var token = '';
  var relativeIndex = 0;

  for (var i = 0; i < total * options.wordsPerString; i++) {
    if (relativeIndex === options.wordsPerString - 1) {
      token += options.formatter(word(useWords), relativeIndex);
    } else {
      token +=
        options.formatter(word(useWords), relativeIndex) + options.separator;
    }
    relativeIndex++;
    if ((i + 1) % options.wordsPerString === 0) {
      results.push(token);
      token = '';
      relativeIndex = 0;
    }
  }
  if (typeof options.join === 'string') {
    results = results.join(options.join);
  }

  return results;
}

export default words;
